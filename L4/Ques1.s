	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors 
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA MYCODE,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R1,[R0]
	LDR R2,=DST
	MOV R3,#1
	MOV R4,#0xA
	MOV R5,#0
	MOV R6,#0xF
	MOV R7,R1
UP	AND R7,R6
	MLA R5,R7,R3,R5
	MUL R3,R4
	LSR R1,#4
	CMP R1,#0
	BNE UP
	STR R5,[R2]
STOP	B STOP
SRC DCD 0x33
	AREA MYDATA,DATA,READWRITE
DST DCD 0
	END