	AREA RESET,DATA,READONLY 
	EXPORT __Vectors 
__Vectors 
	DCD 0X10001000 
	DCD Reset_Handler 
	ALIGN 
	AREA MYCODE,CODE,READONLY 
	ENTRY
	EXPORT Reset_Handler 
Reset_Handler
	LDR R0,=NUM
	LDR R1,=DEN
	LDR R3,=RES
	MOV R4,#0   ;COUNTER/QOUTIENT
	LDR R5,[R0] 
	LDR R6,[R1]
UP	CMP R5,R6
	BCC STORE
	SUB R5,R6
	ADD R4,#1
	B UP
STORE	STR R4,[R3],#4            
		STR R5,[R3]
STOP	B STOP
NUM DCD 161
DEN DCD 3
	AREA mydata,DATA,READWRITE
RES DCD 0
	END