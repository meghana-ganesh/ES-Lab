	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N
	LDR R1,=RES
	LDR R2,[R0]
	MLA R4,R2,R2,R2
	LSR R4,#1
	STR R4,[R1]
STOP	B STOP
N DCD 5
	AREA mydata,DATA,READWRITE
RES DCD 0
	END