	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =VAL
	LDR R1, =DST
	MOV R2,#10
	MOV R3,#0
	MOV R6,#0
UP	LDR R4,[R0],#4
	ADDS R3,R6
	MOV R6,#0
	ADC R6,#0
	ADDS R3,R4
	ADC R6,#0
	SUBS R2,#1
	BNE UP	
	STR R6,[R1],#4
	STR R3,[R1]
	
STOP
	B STOP
VAL DCD 90,90,90,90,90,90,90,90,90,90
	AREA mydata, DATA, READWRITE
DST DCD 0
	END